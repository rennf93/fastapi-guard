def generate_test_suite(output_file: str = "test_suite.json") -> None:
    """Generate comprehensive test suite."""
    test_cases = []

    # Add malicious cases
    for i, text in enumerate(MALICIOUS_CASES):
        test_cases.append({
            "text": text,
            "is_malicious": True,
            "description": f"Malicious case {i+1}",
            "severity": "high" if i % 3 == 0 else "medium",
        })

    # Add benign cases
    for i, text in enumerate(BENIGN_CASES):
        test_cases.append({
            "text": text,
            "is_malicious": False,
            "description": f"Benign case {i+1}",
            "severity": "low",
        })

    # Create test suite
    test_suite = {
        "description": "Comprehensive test suite for prompt injection detection",
        "version": "1.0.0",
        "total_cases": len(test_cases),
        "malicious_cases": len(MALICIOUS_CASES),
        "benign_cases": len(BENIGN_CASES),
        "test_cases": test_cases,
    }

    # Save to file
    output_path = Path(__file__).parent.parent / "prompt_injection" / output_file
    output_path.parent.mkdir(parents=True, exist_ok=True)

    with output_path.open("w") as f:
        json.dump(test_suite, f, indent=2)

    print(f"âœ… Generated test suite with {len(test_cases)} cases")
    print(f"   - Malicious: {len(MALICIOUS_CASES)}")
    print(f"   - Benign: {len(BENIGN_CASES)}")
    print(f"   - Saved to: {output_path}")


if __name__ == "__main__":
    generate_test_suite()
